    public int uniquePaths(int m, int n) {
        //è®°å½•åˆ°è¾¾æ¯ä¸ªç‚¹çš„æ–¹æ³•æ•°,é˜²æ­¢é‡å¤è®¡ç®—
        int[][] visited = new int[m][n];
        return dp(m - 1, n - 1, visited);
    }

    private int dp(int m, int n, int[][] visited) {
        //é˜²æ­¢é‡å¤
        if (visited[m][n] >= 1) {
            return visited[m][n];
        }
        //System.out.println(String.format("m:%d,n:%d", m, n));
        //1.è·³å‡ºæ¡ä»¶
        if (m == 0 && n == 0) {
            return 1;
        }
        //2.å‘ä¸Šæˆ–å‘å·¦çš„æ‹†è§£
        int sum = 0;
        if (m >= 1) {
            visited[m - 1][n] = dp(m - 1, n, visited);
            sum += visited[m - 1][n];
        }
        if (n >= 1) {
            visited[m][n - 1] = dp(m, n - 1, visited);
            sum += visited[m][n - 1];
        }
        return sum;
    }


